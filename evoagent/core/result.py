"""AgentResult, LLMRequest, and LLMResponse types."""

from typing import Any

from pydantic import BaseModel, Field

from evoagent.core.message import Message, ToolCall
from evoagent.core.state import RuntimeState


class LLMRequest(BaseModel):
    """A request to an LLM provider."""

    messages: list[Message] = Field(
        default_factory=list,
        description="Messages to send to the LLM.",
    )
    tools: list[dict[str, Any]] | None = Field(
        default=None,
        description="Function-calling tool schemas.",
    )
    model: str = Field(default="deepseek-chat", description="Model name.")
    temperature: float = Field(default=0.0, description="Sampling temperature.")
    max_tokens: int = Field(default=4096, description="Max tokens in response.")
    stop: list[str] | None = Field(default=None, description="Stop sequences.")
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Extra metadata.",
    )


class LLMUsage(BaseModel):
    """Token usage data from an LLM response."""

    prompt_tokens: int = 0
    completion_tokens: int = 0
    total_tokens: int = 0


class LLMResponse(BaseModel):
    """A response from an LLM provider."""

    content: str = Field(default="", description="Text content from the LLM.")
    tool_calls: list[ToolCall] | None = Field(
        default=None,
        description="Tool calls generated by the LLM, if any.",
    )
    finish_reason: str = Field(
        default="stop",
        description="Why the LLM stopped: stop, tool_calls, length, or error.",
    )
    usage: LLMUsage = Field(default_factory=LLMUsage, description="Token usage.")
    model: str = Field(default="", description="Model used for this response.")
    latency_ms: int = Field(default=0, description="Round-trip latency in milliseconds.")


class AgentResult(BaseModel):
    """The final result of an agent execution."""

    run_id: str = Field(..., description="ID of the completed run.")
    task: str = Field(default="", description="The original task.")
    success: bool = Field(..., description="Whether the task succeeded.")
    final_answer: str = Field(default="", description="Final answer text from the agent.")
    state: RuntimeState | None = Field(
        default=None,
        description="Final runtime state snapshot.",
    )
    steps_taken: int = Field(default=0, description="Number of steps executed.")
    tool_calls: int = Field(default=0, description="Number of tool calls made.")
    total_tokens: int = Field(default=0, description="Total tokens used.")
    total_cost: float = Field(default=0.0, description="Estimated cost in USD.")
    duration_ms: int = Field(default=0, description="Total wall-clock duration.")
    events_path: str | None = Field(
        default=None,
        description="Path to the event log for this run.",
    )
    error: str | None = Field(
        default=None,
        description="Error message if the run failed.",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Extra metadata.",
    )


